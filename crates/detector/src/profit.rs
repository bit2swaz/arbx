    // ── Helpers ──────────────────────────────────────────────────────────────

    fn addr(seed: u8) -> Address {
        Address::from([seed; 20])
    }

    fn make_strategy() -> StrategyConfig {
        StrategyConfig {
            min_profit_floor_usd: 0.50,
            gas_buffer_multiplier: 1.1,
            max_gas_gwei: 0.1,
            flash_loan_fee_bps: 0,
        }
    }

    /// Construct a GasEstimate with specific numeric components.
    fn make_gas_estimate(
        l2_gas: u64,
        l2_price: u128,
        l1_gas: u64,
        l1_base: u128,
        eth_price: f64,
    ) -> GasEstimate {
        let l2_cost = U256::from(l2_gas as u128 * l2_price);
        let l1_cost = U256::from(l1_gas as u128 * l1_base);
        let total = l2_cost + l1_cost;
        let total_usd = if eth_price > 0.0 {
            (l2_gas as f64 * l2_price as f64 + l1_gas as f64 * l1_base as f64) / 1e18 * eth_price
        } else {
            0.0
        };
        GasEstimate {
            l2_gas_units: l2_gas,
            l2_gas_price_wei: l2_price,
            l2_cost_wei: l2_cost,
            l1_calldata_gas: l1_gas,
            l1_base_fee_wei: l1_base,
            l1_cost_wei: l1_cost,
            total_cost_wei: total,
            total_cost_usd: total_usd,
        }
    }

    /// Construct a GasEstimate with only total_cost_wei set (others zeroed).
    fn gas_with_total(total: U256) -> GasEstimate {
        GasEstimate {
            l2_gas_units: 0,
            l2_gas_price_wei: 0,
            l2_cost_wei: U256::ZERO,
            l1_calldata_gas: 0,
            l1_base_fee_wei: 0,
            l1_cost_wei: U256::ZERO,
            total_cost_wei: total,
            total_cost_usd: 0.0,
        }
    }

    /// Build a mock with all five methods returning known constants.
    fn make_mock(
        l2_gas: u64,
        l2_price: u128,
        l1_gas: u64,
        l1_base: u128,
        eth_price: f64,
    ) -> MockGasFetcher {
        let mut mock = MockGasFetcher::new();
        mock.expect_estimate_l2_gas()
            .returning(move |_, _| Ok(l2_gas));
        mock.expect_l2_gas_price_wei()
            .returning(move || Ok(l2_price));
        mock.expect_estimate_l1_gas()
            .returning(move |_, _| Ok(l1_gas));
        mock.expect_l1_base_fee_wei().returning(move || Ok(l1_base));
        mock.expect_eth_price_usd().returning(move || eth_price);
        mock
    }

    /// Minimal ArbPath for filter tests.
    fn make_path(estimated_profit_wei: U256) -> ArbPath {
        ArbPath {
            token_in: addr(1),
            pool_a: addr(10),
            token_mid: addr(2),
            pool_b: addr(11),
            token_out: addr(1),
            estimated_profit_wei,
            flash_loan_amount_wei: U256::from(1_000_000u64),
        }
    }

    // ── Unit tests ────────────────────────────────────────────────────────────

    /// estimate_gas correctly sums L2 and L1 components.
    ///
    /// l2_cost = 200_000 × 100 gwei  = 20_000_000_000_000_000 wei
    /// l1_cost =   5_000 ×  20 gwei  =        100_000_000_000_000 wei
    /// total                         = 20_100_000_000_000_000 wei
    #[tokio::test]
    async fn test_gas_estimate_sums_l2_and_l1() {
        const L2_GAS: u64 = 200_000;
        const L2_PRICE: u128 = 100_000_000_000; // 100 gwei
        const L1_GAS: u64 = 5_000;
        const L1_BASE: u128 = 20_000_000_000; // 20 gwei

        let mock = make_mock(L2_GAS, L2_PRICE, L1_GAS, L1_BASE, 2000.0);
        let calc = ProfitCalculator::new(mock, make_strategy());

        let gas = calc.estimate_gas(addr(10), Bytes::new()).await.unwrap();

        let expected_l2 = U256::from(L2_GAS as u128 * L2_PRICE); // 20_000_000_000_000_000
        let expected_l1 = U256::from(L1_GAS as u128 * L1_BASE); //        100_000_000_000_000
        let expected_total = expected_l2 + expected_l1; //         20_100_000_000_000_000

        assert_eq!(gas.l2_cost_wei, expected_l2, "L2 cost mismatch");
        assert_eq!(gas.l1_cost_wei, expected_l1, "L1 cost mismatch");
        assert_eq!(gas.total_cost_wei, expected_total, "total cost mismatch");
    }

    /// compute_min_profit_wei includes both the buffer and the USD floor.
    ///
    /// floor_in_wei = 0.50 / 3000 × 1e18 ≈ 166_666_666_666_666 wei
    /// gas_cost     = 1_000_000_000_000_000 wei
    /// buffered     = gas_cost × 1.1       = 1_100_000_000_000_000 wei
    /// expected_min = buffered + floor      ≈ 1_266_666_666_666_666 wei
    #[test]
    fn test_min_profit_includes_floor() {
        let mut mock = MockGasFetcher::new();
        mock.expect_eth_price_usd().returning(|| 3000.0);

        let calc = ProfitCalculator::new(mock, make_strategy());
        let gas = gas_with_total(U256::from(1_000_000_000_000_000u64));
        let min = calc.compute_min_profit_wei(&gas);

        let expected = U256::from(1_266_666_666_666_666u128);
        // Allow ≤1 wei tolerance for f64 truncation of the floor term.
        let diff = if min >= expected {
            min - expected
        } else {
            expected - min
        };
        assert!(
            diff <= U256::from(1u64),
            "expected ≈{expected}, got {min} (diff={diff})"
        );
    }

    /// A path whose profit is 2× the minimum threshold passes the filter.
    #[tokio::test]
    async fn test_profitable_path_passes_filter() {
        const L2_GAS: u64 = 200_000;
        const L2_PRICE: u128 = 100_000_000_000;
        const L1_GAS: u64 = 5_000;
        const L1_BASE: u128 = 20_000_000_000;
        const ETH_PRICE: f64 = 2000.0;

        // Compute min_profit directly (pure, no mock) to set the path profit.
        let total = U256::from(L2_GAS as u128 * L2_PRICE + L1_GAS as u128 * L1_BASE);
        let min_profit = compute_min_profit_pure(total, 1.1, 0.50, ETH_PRICE);

        // filter() needs a mock that returns all gas values.
        let mock = make_mock(L2_GAS, L2_PRICE, L1_GAS, L1_BASE, ETH_PRICE);
        let calc = ProfitCalculator::new(mock, make_strategy());
        let path = make_path(min_profit * U256::from(2u32));

        let result = calc.filter(&path, Bytes::new()).await.unwrap();
        assert!(result.is_some(), "2× min_profit should pass the filter");
    }

    /// A path whose profit is half the minimum threshold is rejected.
