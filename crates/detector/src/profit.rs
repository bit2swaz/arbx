// ─── Pure computation helper ─────────────────────────────────────────────────

/// Pure (no I/O) minimum-profit computation — exported for property tests.
///
/// ```text
/// min_profit = total_gas_cost × gas_buffer_multiplier
///            + (min_profit_floor_usd / eth_price_usd) × 10^18
/// ```
///
/// Integer scaling (`× 1_000_000_000 / 1_000_000_000`) is used for the buffer
/// multiply to avoid f64 precision drift on large U256 values.
pub fn compute_min_profit_pure(
    total_cost_wei: U256,
    gas_buffer_multiplier: f64,
    min_profit_floor_usd: f64,
    eth_price_usd: f64,
) -> U256 {
    // Scale the multiplier into an integer to stay in U256 arithmetic.
    // E.g.  1.1  → 1_100_000_000  → divide by 1_000_000_000 at the end.
    let buffer_num = (gas_buffer_multiplier * 1_000_000_000.0) as u128;
    let buffered_gas = (total_cost_wei * U256::from(buffer_num)) / U256::from(1_000_000_000u128);

    let floor_wei = if eth_price_usd > 0.0 {
        U256::from((min_profit_floor_usd / eth_price_usd * 1e18) as u128)
    } else {
        U256::ZERO
    };

    buffered_gas + floor_wei
}

// ─── ProfitCalculator ────────────────────────────────────────────────────────

/// Evaluates candidate arb paths against the Arbitrum 2D-gas dynamic threshold.
///
/// The threshold adapts automatically to mainnet gas spikes: during quiet
/// periods the floor dominates; during congestion the L1 calldata cost
/// dominates, raising the bar so the bot never submits losing transactions.
pub struct ProfitCalculator<G: GasFetcher> {
    fetcher: G,
    strategy: StrategyConfig,
}

impl<G: GasFetcher> ProfitCalculator<G> {
    /// Constructs a new calculator.
    pub fn new(fetcher: G, strategy: StrategyConfig) -> Self {
        Self { fetcher, strategy }
    }

    /// Fetches both L2 execution gas and L1 calldata gas, assembles a full
    /// [`GasEstimate`] with both cost components and the USD equivalent.
    pub async fn estimate_gas(&self, to: Address, calldata: Bytes) -> anyhow::Result<GasEstimate> {
        let l2_gas_units = self
            .fetcher
            .estimate_l2_gas(to, calldata.clone())
            .await
            .context("estimate_l2_gas failed")?;
        let l2_price = self
            .fetcher
            .l2_gas_price_wei()
            .await
            .context("l2_gas_price_wei failed")?;
        let l1_gas_units = self
            .fetcher
            .estimate_l1_gas(to, calldata)
            .await
            .context("estimate_l1_gas failed")?;
        let l1_base = self
            .fetcher
            .l1_base_fee_wei()
            .await
            .context("l1_base_fee_wei failed")?;
        let eth_price = self.fetcher.eth_price_usd();

        let l2_cost = U256::from(l2_gas_units as u128 * l2_price);
        let l1_cost = U256::from(l1_gas_units as u128 * l1_base);
        let total_cost = l2_cost + l1_cost;

        // total_cost_usd is informational (used for observability logging).
        let total_cost_usd = if eth_price > 0.0 {
            let wei_f64 =
                (l2_gas_units as f64 * l2_price as f64) + (l1_gas_units as f64 * l1_base as f64);
            wei_f64 / 1e18 * eth_price
        } else {
            0.0
        };

        Ok(GasEstimate {
            l2_gas_units,
            l2_gas_price_wei: l2_price,
            l2_cost_wei: l2_cost,
            l1_calldata_gas: l1_gas_units,
            l1_base_fee_wei: l1_base,
            l1_cost_wei: l1_cost,
            total_cost_wei: total_cost,
            total_cost_usd,
        })
    }

    /// Computes the minimum acceptable profit in wei for a given gas estimate.
    ///
    /// Delegates to [`compute_min_profit_pure`] with the cached ETH price and
    /// current strategy parameters.
    pub fn compute_min_profit_wei(&self, gas: &GasEstimate) -> U256 {
        compute_min_profit_pure(
            gas.total_cost_wei,
            self.strategy.gas_buffer_multiplier,
            self.strategy.min_profit_floor_usd,
            self.fetcher.eth_price_usd(),
        )
    }

    /// Returns `Some(Opportunity)` only when `path.estimated_profit_wei` is
    /// **strictly greater** than `compute_min_profit_wei`.
    ///
    /// Gas is estimated against `path.pool_a` — the first contract the
    /// on-chain transaction touches.
    pub async fn filter(
        &self,
        path: &ArbPath,
        calldata: Bytes,
    ) -> anyhow::Result<Option<Opportunity>> {
        let gas = self
            .estimate_gas(path.pool_a, calldata)
            .await
            .context("gas estimation failed in filter")?;

        let min_profit = self.compute_min_profit_wei(&gas);

        if path.estimated_profit_wei <= min_profit {
            debug!(
                pool_a  = %path.pool_a,
                profit  = %path.estimated_profit_wei,
                min     = %min_profit,
                "path below profit threshold — skipping"
            );
            return Ok(None);
        }

        let net_profit_wei = path.estimated_profit_wei.saturating_sub(gas.total_cost_wei);
        let detected_at_ms = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_millis() as u64;

        Ok(Some(Opportunity {
            path: path.clone(),
            gross_profit_wei: path.estimated_profit_wei,
            l2_gas_cost_wei: gas.l2_cost_wei,
            l1_gas_cost_wei: gas.l1_cost_wei,
            net_profit_wei,
            detected_at_ms,
        }))
    }
}

