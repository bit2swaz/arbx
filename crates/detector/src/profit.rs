//! Profit calculator with Arbitrum 2D gas model (L2 execution + L1 calldata).
//!
//! # Why two-dimensional gas matters
//! On Arbitrum every transaction pays two costs:
//! 1. **L2 execution gas** — what `eth_estimateGas` returns.
//! 2. **L1 calldata gas** — cost of posting the tx back to Ethereum mainnet.
//!
//! During mainnet congestion the L1 component can reach $1.50+, silently
//! turning a profitable simulation into an on-chain loss.  The
//! [`GasFetcher::estimate_l1_gas`] method calls the Arbitrum NodeInterface
//! precompile at `0x…C8` via `gasEstimateL1Component` to get the real cost.
//!
//! # Design for testability
//! All RPC calls are routed through the [`GasFetcher`] trait.  Tests inject
//! `MockGasFetcher` (generated by mockall) and never touch the network.

use std::sync::{Arc, RwLock};

use alloy::network::Ethereum;
use alloy::primitives::{Address, Bytes, U256};
use alloy::providers::RootProvider;
use anyhow::Context as _;
use arbx_common::config::StrategyConfig;
use arbx_common::types::{ArbPath, GasEstimate, Opportunity};
use tracing::debug;

// ─── GasFetcher trait ────────────────────────────────────────────────────────

/// Abstraction over all RPC calls made by the profit calculator.
///
/// `#[cfg_attr(test, mockall::automock)]` generates `MockGasFetcher` in test
/// builds so that every test runs without a live Arbitrum node.
///
/// `eth_price_usd` is intentionally **non-async**: it returns a cached value
/// updated by a background task, so no network round-trip is needed on the
/// hot path.
#[cfg_attr(test, mockall::automock)]
#[async_trait::async_trait]
pub trait GasFetcher: Send + Sync {
    /// Current L2 execution gas price in wei.
    async fn l2_gas_price_wei(&self) -> anyhow::Result<u128>;

    /// Estimates L2 execution gas units for the given call.
    async fn estimate_l2_gas(&self, to: Address, data: Bytes) -> anyhow::Result<u64>;

    /// Estimates L1 calldata gas units via Arbitrum's NodeInterface precompile
    /// (`0x00…C8`, function `gasEstimateL1Component`).
    async fn estimate_l1_gas(&self, to: Address, data: Bytes) -> anyhow::Result<u64>;

    /// L1 base fee in wei (from NodeInterface `l1BaseFeeEstimate`).
    async fn l1_base_fee_wei(&self) -> anyhow::Result<u128>;

    /// Cached ETH/USD spot price — synchronous, updated by a background task.
    fn eth_price_usd(&self) -> f64;
}

// ─── AlloyGasFetcher ─────────────────────────────────────────────────────────

/// Production [`GasFetcher`] backed by an alloy [`RootProvider<Ethereum>`].
///
/// Uses Arbitrum's NodeInterface precompile at address
/// `0x00000000000000000000000000000000000000C8` for the L1 cost component.
pub struct AlloyGasFetcher {
    provider: Arc<RootProvider<Ethereum>>,
    /// Arbitrum NodeInterface precompile (`0x00…C8`).
    node_interface: Address,
    /// Periodically refreshed by a background price-feed task.
    eth_price_usd: Arc<RwLock<f64>>,
}

impl AlloyGasFetcher {
    /// Constructs a new fetcher.
    ///
    /// `eth_price_usd` must be refreshed externally (e.g. via a CoinGecko
    /// price-feed task) — the calculator reads it on every profitability check.
    pub fn new(
        provider: Arc<RootProvider<Ethereum>>,
        node_interface: Address,
        eth_price_usd: Arc<RwLock<f64>>,
    ) -> Self {
        Self {
            provider,
            node_interface,
            eth_price_usd,
        }
    }
}

