    /// Exact-at-threshold ⟹ None; one-wei-above ⟹ Some.
    #[tokio::test]
    async fn test_exact_threshold_boundary() {
        const L2_GAS: u64 = 200_000;
        const L2_PRICE: u128 = 100_000_000_000;
        const L1_GAS: u64 = 5_000;
        const L1_BASE: u128 = 20_000_000_000;
        const ETH_PRICE: f64 = 2000.0;

        let total = U256::from(L2_GAS as u128 * L2_PRICE + L1_GAS as u128 * L1_BASE);
        let min_profit = compute_min_profit_pure(total, 1.1, 0.50, ETH_PRICE);

        // === profit == min_profit → None ===
        let mock1 = make_mock(L2_GAS, L2_PRICE, L1_GAS, L1_BASE, ETH_PRICE);
        let calc1 = ProfitCalculator::new(mock1, make_strategy());
        let result_exact = calc1
            .filter(&make_path(min_profit), Bytes::new())
            .await
            .unwrap();
        assert!(
            result_exact.is_none(),
            "profit == min_profit must return None (not strictly profitable)"
        );

        // === profit == min_profit + 1 → Some ===
        let mock2 = make_mock(L2_GAS, L2_PRICE, L1_GAS, L1_BASE, ETH_PRICE);
        let calc2 = ProfitCalculator::new(mock2, make_strategy());
        let result_above = calc2
            .filter(&make_path(min_profit + U256::from(1u32)), Bytes::new())
            .await
            .unwrap();
        assert!(
            result_above.is_some(),
            "profit == min_profit + 1 must return Some"
        );
    }

    // ── Property tests ────────────────────────────────────────────────────────

    mod property_tests {
        use super::*;
        use proptest::prelude::*;

        proptest! {
            /// Any valid gas estimate always produces a strictly positive minimum profit
            /// (gas buffer × cost + floor is always > 0).
            #[test]
            fn prop_threshold_always_positive(
                total_gas_wei in 0u128..100_000_000_000_000_000u128,  // up to ~0.1 ETH
                eth_price     in 100.0f64..20_000.0f64,
            ) {
                let min = compute_min_profit_pure(
                    U256::from(total_gas_wei),
                    1.1,
                    0.50,
                    eth_price,
                );
                // min_profit must be strictly positive:
                // even if gas_cost=0, the floor term (0.50 USD / eth_price * 1e18) > 0.
                prop_assert!(min > U256::ZERO, "threshold must always be positive, got {min}");
            }

            /// Strictly monotone: higher total gas cost ⟹ higher threshold.
            ///
            /// The threshold is linear in `total_cost_wei` (buffer term dominates
            /// for large gas; floor is constant), so any increase in gas must
            /// raise the threshold.
            #[test]
            fn prop_higher_gas_higher_threshold(
                gas_a in 1u128..50_000_000_000_000_000u128,
                gas_b in 1u128..50_000_000_000_000_000u128,
            ) {
                // Ensure a < b.
                let (lo, hi) = if gas_a <= gas_b { (gas_a, gas_b) } else { (gas_b, gas_a) };
                if lo == hi {
                    return Ok(()); // skip degenerate case
                }

                let threshold_lo =
                    compute_min_profit_pure(U256::from(lo), 1.1, 0.50, 3000.0);
                let threshold_hi =
                    compute_min_profit_pure(U256::from(hi), 1.1, 0.50, 3000.0);

                prop_assert!(
                    threshold_hi > threshold_lo,
                    "higher gas must yield higher threshold: lo={lo}, hi={hi}, \
                     threshold_lo={threshold_lo}, threshold_hi={threshold_hi}"
                );
            }
        }
    }
