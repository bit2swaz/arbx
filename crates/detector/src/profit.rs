    /// L1 gas spike (1000× baseline) raises the threshold by more than 100×.
    ///
    /// Uses an astronomical ETH price so the USD floor term (`$0.50 / ETH_PRICE`)
    /// becomes negligible in wei, letting us measure the pure L1-cost ratio
    /// without the constant floor overwhelming the baseline.
    #[test]
    fn test_gas_spike_raises_threshold() {
        // $10 M / ETH → floor_wei ≈ 5e10 wei — negligible vs gas costs.
        const ETH_PRICE: f64 = 10_000_000.0;

        // baseline: 1_000 L1 units × 5 gwei = 5_000_000_000_000 wei
        let baseline_total = U256::from(1_000u128 * 5_000_000_000u128);
        let threshold_baseline = compute_min_profit_pure(baseline_total, 1.1, 0.50, ETH_PRICE);

        // spike: 100_000 L1 units × 50 gwei = 5_000_000_000_000_000 wei (1_000× more)
        let spike_total = U256::from(100_000u128 * 50_000_000_000u128);
        let threshold_spike = compute_min_profit_pure(spike_total, 1.1, 0.50, ETH_PRICE);

        assert!(
            threshold_spike > threshold_baseline * U256::from(100u32),
            "gas spike (1000×) should raise threshold >100×: \
             baseline={threshold_baseline}, spike={threshold_spike}"
        );
    }

